//! Render `index.js`: the `CommonJS` wrapper around the native addon.
//!
//! The compiled glue only guarantees the low-level channel: camelCased
//! napi exports and `__unibind__:` rejection reasons. This wrapper is the
//! user surface: it decodes reasons into the generated `Error`
//! subclasses, turns stream handles into `AsyncIterable`s (early exit
//! closes the producer), wraps object handles into classes with the
//! resource close surface (`await using` works), and assigns each export
//! individually so Node's cjs-module-lexer sees named exports.

use std::fmt::Write as _;

use unibind_core::ir;

use super::types::{self, doc_block, resource_close, type_name, value_name};

pub fn render(interface: &ir::Interface, addon: &str) -> String {
    let mut out = String::new();
    prelude(&mut out, addon);
    for error in &interface.errors {
        error_classes(&mut out, error);
    }
    decoder(&mut out, interface);
    if types::has_streams(interface) {
        out.push_str(STREAM_HELPER);
    }
    if !interface.objects.is_empty() {
        out.push_str(HANDLE_TOKEN);
    }
    for object in &interface.objects {
        object_class(&mut out, object);
    }
    for function in &interface.functions {
        function_wrapper(&mut out, interface, function);
    }
    exports(&mut out, interface);
    out
}

fn prelude(out: &mut String, addon: &str) {
    out.push_str("\"use strict\";\n\n");
    out.push_str(
        "// Generated by `unibind-gen ts` from the interface embedded in the\n\
         // native addon; do not edit. The packaging step places the compiled\n",
    );
    writeln!(out, "// addon at ./native/{addon}.node next to this file.\n").expect("write to string");
    writeln!(out, "const native = require(\"./native/{addon}.node\");\n").expect("write to string");
}

/// The base class carries `code` (the variant subclass name); each variant
/// subclass pins both `name` and `code`.
fn error_classes(out: &mut String, error: &ir::ErrorType) {
    let base = type_name(&error.names, &error.name);
    doc_block(out, "", &error.docs);
    writeln!(out, "class {base} extends Error {{").expect("write to string");
    out.push_str("  constructor(message, code) {\n    super(message);\n");
    writeln!(out, "    this.name = \"{base}\";").expect("write to string");
    out.push_str("    this.code = code;\n  }\n}\n");
    for variant in &error.variants {
        let name = type_name(&variant.names, &variant.name);
        doc_block(out, "", &variant.docs);
        writeln!(out, "class {name} extends {base} {{").expect("write to string");
        out.push_str("  constructor(message) {\n");
        writeln!(out, "    super(message, \"{name}\");").expect("write to string");
        writeln!(out, "    this.name = \"{name}\";").expect("write to string");
        out.push_str("  }\n}\n");
    }
    out.push('\n');
}

const ABORT_HELPER: &str = "\
// Node's convention for cancelled operations: an error named AbortError.
function makeAbortError() {
  if (typeof DOMException === \"function\") {
    return new DOMException(\"This operation was aborted\", \"AbortError\");
  }
  const error = new Error(\"This operation was aborted\");
  error.name = \"AbortError\";
  return error;
}

";

const DECODE_HEAD: &str = "\
// Rethrow native failures as the generated classes; anything without the
// `__unibind__:` reason prefix passes through untouched.
function decodeError(error) {
  const reason =
    error instanceof Error && typeof error.message === \"string\" ? error.message : null;
  if (reason === null || !reason.startsWith(\"__unibind__:\")) {
    return error;
  }
  const rest = reason.slice(\"__unibind__:\".length);
  if (rest === \"aborted\") {
    return makeAbortError();
  }
";

const DECODE_TAIL: &str = "\
  if (rest.startsWith(\"err:\")) {
    const body = rest.slice(\"err:\".length);
    const nameEnd = body.indexOf(\":\");
    const variantEnd = body.indexOf(\":\", nameEnd + 1);
    if (nameEnd !== -1 && variantEnd !== -1) {
      const decode = (errorVariants[body.slice(0, nameEnd)] ?? {})[
        body.slice(nameEnd + 1, variantEnd)
      ];
      if (decode !== undefined) {
        return decode(body.slice(variantEnd + 1));
      }
    }
  }
  return error;
}

";

/// The decoder table (keyed by the Rust names spelled inside reasons), the
/// abort helper, and `decodeError` itself.
fn decoder(out: &mut String, interface: &ir::Interface) {
    out.push_str("// Rust `<enum>` and `<variant>` names as spelled inside rejection reasons.\n");
    out.push_str("const errorVariants = {\n");
    for error in &interface.errors {
        writeln!(out, "  {}: {{", error.name).expect("write to string");
        for variant in &error.variants {
            writeln!(
                out,
                "    {}: (message) => new {}(message),",
                variant.name,
                type_name(&variant.names, &variant.name)
            )
            .expect("write to string");
        }
        out.push_str("  },\n");
    }
    out.push_str("};\n\n");
    out.push_str(ABORT_HELPER);
    out.push_str(DECODE_HEAD);
    out.push_str(DECODE_TAIL);
}

const STREAM_HELPER: &str = "\
// Wrap the native pull handle into an AsyncIterable; leaving iteration
// early (break, return, throw) closes the stream, so the Rust producer
// sees its stream dropped and stops.
function wrapStream(handle) {
  const stream = {
    async next() {
      try {
        return await handle.next();
      } catch (error) {
        throw decodeError(error);
      }
    },
    close() {
      handle.close();
    },
    async *[Symbol.asyncIterator]() {
      try {
        for (;;) {
          const item = await stream.next();
          if (item === null) {
            return;
          }
          yield item;
        }
      } finally {
        stream.close();
      }
    },
  };
  return stream;
}

";

const HANDLE_TOKEN: &str = "\
// Internal marker: wrapper classes constructed around an existing native
// handle (function returns) instead of through the native constructor.
const nativeHandle = Symbol(\"unibind.nativeHandle\");

";

/// The wrapper class over one native object handle: the constructor (when
/// the object declares one), delegating methods with error decoding, and
/// the resource close surface with `await using` disposal.
fn object_class(out: &mut String, object: &ir::Object) {
    let class = type_name(&object.names, &object.name);
    doc_block(out, "", &object.docs);
    writeln!(out, "class {class} {{").expect("write to string");
    out.push_str("  #handle;\n\n");
    if let Some(ctor) = &object.constructor {
        doc_block(out, "  ", &ctor.docs);
        out.push_str("  constructor(...args) {\n");
        out.push_str("    if (args[0] === nativeHandle) {\n      this.#handle = args[1];\n      return;\n    }\n");
        out.push_str("    try {\n");
        writeln!(out, "      this.#handle = new native.{class}(...args);").expect("write to string");
        out.push_str("    } catch (error) {\n      throw decodeError(error);\n    }\n  }\n");
    } else {
        out.push_str("  /** Instances come from the exported functions returning this type. */\n");
        out.push_str("  constructor(...args) {\n");
        out.push_str("    if (args[0] !== nativeHandle) {\n");
        writeln!(
            out,
            "      throw new TypeError(\"{class} instances come from the exported functions that return them\");"
        )
        .expect("write to string");
        out.push_str("    }\n    this.#handle = args[1];\n  }\n");
    }
    let close = resource_close(object);
    for method in &object.methods {
        if close.is_some_and(|close| std::ptr::eq(close, method)) {
            continue;
        }
        method_delegation(out, method, &value_name(&method.name, &method.names));
    }
    if let Some(close) = close {
        // The generated native close is idempotent; delegate it like any
        // method, then wire disposal through it.
        method_delegation(out, close, "close");
        out.push_str("\n  /** `await using` support: closes the resource. */\n");
        out.push_str("  async [Symbol.asyncDispose]() {\n    await this.close();\n  }\n");
    }
    out.push_str("}\n\n");
}

/// One delegating method: forward the positional arguments (async methods
/// take the optional trailing `AbortSignal` natively) and decode failures.
fn method_delegation(out: &mut String, method: &ir::Function, name: &str) {
    out.push('\n');
    doc_block(out, "  ", &method.docs);
    match method.asyncness {
        ir::Asyncness::Sync => {
            writeln!(out, "  {name}(...args) {{\n    try {{").expect("write to string");
            writeln!(out, "      return this.#handle.{name}(...args);").expect("write to string");
        }
        ir::Asyncness::Async => {
            writeln!(out, "  async {name}(...args) {{\n    try {{").expect("write to string");
            writeln!(out, "      return await this.#handle.{name}(...args);").expect("write to string");
        }
    }
    out.push_str("    } catch (error) {\n      throw decodeError(error);\n    }\n  }\n");
}

/// One exported function: forward the positional arguments (async exports
/// take the optional trailing `AbortSignal` natively), decode failures, and
/// wrap stream and object returns.
fn function_wrapper(out: &mut String, interface: &ir::Interface, function: &ir::Function) {
    let name = value_name(&function.name, &function.names);
    doc_block(out, "", &function.docs);
    let is_async = matches!(function.asyncness, ir::Asyncness::Async);
    let call = if is_async {
        format!("await native.{name}(...args)")
    } else {
        format!("native.{name}(...args)")
    };
    let value = match &function.ret {
        Some(ir::Type::Stream(_)) => format!("wrapStream({call})"),
        Some(ir::Type::Named(named)) => interface
            .objects
            .iter()
            .find(|object| object.name == *named)
            .map_or_else(
                || call.clone(),
                |object| {
                    format!("new {}(nativeHandle, {call})", type_name(&object.names, &object.name))
                },
            ),
        _ => call,
    };
    if is_async {
        writeln!(out, "async function {name}(...args) {{").expect("write to string");
    } else {
        writeln!(out, "function {name}(...args) {{").expect("write to string");
    }
    out.push_str("  try {\n");
    writeln!(out, "    return {value};").expect("write to string");
    out.push_str("  } catch (error) {\n    throw decodeError(error);\n  }\n}\n\n");
}

/// One assignment per export so cjs-module-lexer detects the named-export
/// set and `import { ... }` works from ESM.
fn exports(out: &mut String, interface: &ir::Interface) {
    out.push_str("// Assign each binding as its own property so Node's cjs-module-lexer\n");
    out.push_str("// detects them as named exports.\n");
    for function in &interface.functions {
        let name = value_name(&function.name, &function.names);
        writeln!(out, "module.exports.{name} = {name};").expect("write to string");
    }
    for error in &interface.errors {
        let base = type_name(&error.names, &error.name);
        writeln!(out, "module.exports.{base} = {base};").expect("write to string");
        for variant in &error.variants {
            let name = type_name(&variant.names, &variant.name);
            writeln!(out, "module.exports.{name} = {name};").expect("write to string");
        }
    }
    for object in &interface.objects {
        let class = type_name(&object.names, &object.name);
        writeln!(out, "module.exports.{class} = {class};").expect("write to string");
    }
}
