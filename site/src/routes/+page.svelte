<h1>ix images</h1>

<p>
  Pre-built OCI images and composable NixOS modules for
  <a href="https://ix.dev">ix VMs</a>. Every image targets AMD EPYC Gen 5 (Zen 5)
  and ships with system tools, language toolchains, and service modules ready to
  enable.
</p>

<h2>Repository</h2>

<p>
  Source lives at
  <a href="https://github.com/indexable-inc/index">github.com/indexable-inc/index</a>.
  Images are auto-discovered from <code>images/</code>; modules live under
  <code>modules/services/</code>.
</p>

